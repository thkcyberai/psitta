/// Display-only translations for narrative-structure beat labels and best-for
/// variants. English stays canonical (committed beats + guidance lookup +
/// Story-Coach/Analyzer matching all use the English strings); these translate
/// what the writer sees. Keyed by the exact English string; unknown strings
/// (never expected) fall through unchanged. Framework NAMES are intentionally
/// absent — they stay English.

int _li(String lang) => switch (lang) { 'pt' => 0, 'es' => 1, 'fr' => 2, _ => -1 };

String beatLabel(BuildContext context, String en) {
  final i = _li(Localizations.localeOf(context).languageCode);
  if (i < 0) return en;
  final v = _beatTr[en];
  return v != null ? v[i] : en;
}

String bestForLabel(BuildContext context, String en) {
  final i = _li(Localizations.localeOf(context).languageCode);
  if (i < 0) return en;
  final v = _bestForTr[en];
  return v != null ? v[i] : en;
}

